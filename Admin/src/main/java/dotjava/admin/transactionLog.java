package dotjava.admin;

public class transactionLog {
    private int idTransaction;
    private int idAct;
    private int idItemSold;
    private double cash;
    private double total;

    // Getter for idTransaction
    public int getIdTransaction() {
        return idTransaction;
    }

    // Setter for idTransaction
    public void setIdTransaction(int idTransaction) {
        this.idTransaction = idTransaction;
    }

    // Getter for idAct
    public int getIdAct() {
        return idAct;
    }

    // Setter for idAct
    public void setIdAct(int idAct) {
        this.idAct = idAct;
    }

    // Getter for idItemSold
    public int getIdItemSold() {
        return idItemSold;
    }

    // Setter for idItemSold
    public void setIdItemSold(int idItemSold) {
        this.idItemSold = idItemSold;
    }

    // Getter for cash
    public double getCash() {
        return cash;
    }

    // Setter for cash
    public void setCash(double cash) {
        this.cash = cash;
    }

    // Getter for total
    public double getTotal() {
        return total;
    }

    // Setter for total
    public void setTotal(double total) {
        this.total = total;
    }
}




